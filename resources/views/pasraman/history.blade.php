            lingkungan. Tenaga guru agama ditunjuk diantaranya I Gede Dalem, I
            Gede Sumerta, I Gusti Ambara (belum menjadi Pemangku) dan I Ketut
            Sumitha Sendiri.<br /><br />
            Dalam perkembangannya proses belajar mengajar pada ruang garase
